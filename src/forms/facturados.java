    //OBTENER TOTAL DE DINERO DE LAS FACTURAS
    public void totalf() {

        Double total;
        String consulta = "SELECT SUM(total) FROM facturados;";
        try {
            PreparedStatement prst = cnn.prepareStatement(consulta);
            ResultSet res = prst.executeQuery(consulta);

            if (res.next()) {
                total = res.getDouble(1);

                dinero_fact_txt.setText(Double.toString(total));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "error" + ex);
        }
    }

    //CANTIDAD DE PEDIDOS FACTURADOS
    public void cantf() {

        int cant;
        String consulta = "SELECT COUNT(*) FROM facturados;";
        try {
            PreparedStatement prst = cnn.prepareStatement(consulta);
            ResultSet res = prst.executeQuery(consulta);

            if (res.next()) {
                cant = res.getInt(1);

                ped_fac_txt.setText(Integer.toString(cant));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "error" + ex);
        }
    }

    //REFRESCAR LA TABLA
    public void refrescar() {
        try {
            pedido.setColumnCount(0);
            pedido.setRowCount(0);
            pedido_tbl.revalidate();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex);
        }
    }

//MOSTRAR DATOS DE LA TABLA
    public void mostrar_datos() {

        refrescar();

        pedido.addColumn("Factura");
        pedido.addColumn("Pedido");
        pedido.addColumn("Producto");
        pedido.addColumn("RNC cliente");
        pedido.addColumn("Cliente");
        pedido.addColumn("Total");

        this.pedido_tbl.setModel(pedido);
        String consul;
        consul = "SELECT * FROM facturados ORDER BY nf ASC";

        String[] datos = new String[6];
        try {
            Statement st = cnn.createStatement();
            ResultSet rs = st.executeQuery(consul);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);

                pedido.addRow(datos);

            }

            pedido_tbl.setModel(pedido);
        } catch (SQLException ex) {
            Logger.getLogger(log_form.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error " + ex);
        }

    }

