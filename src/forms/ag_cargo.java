    private void agregar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_btnActionPerformed
        //AGREGAR PUESTO
        String nombre = nombre_txt.getText();
        String departamento = dpto_txt.getText();
       
        String consultacargo = "INSERT INTO puestos (nombre_cargo, nombre_dpto) VALUES ('"+nombre+"', '"+departamento+"');";
        PreparedStatement pss;
        try{
                  pss = cnn.prepareStatement(consultacargo);
                  pss.executeUpdate();
                  
            JOptionPane.showMessageDialog(null,"Agregado");
            this.dispose();
            nombre_txt.setText("");
            dpto_txt.setText("");
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "error " + ex);
            }
    }//GEN-LAST:event_agregar_btnActionPerformed

